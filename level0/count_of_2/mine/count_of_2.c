static int	nums_2s(int n)
{
	if (n < 10)
		return ((n == 2) ? 1 : 0);
	else
		return (nums_2s(n / 10) + nums_2s(n % 10));
		
}

int		count_of_2(int n)
{
	int sum = 0;
	for (int i = 0; i <= n; ++i)
	{
		sum += nums_2s(i);
	}
	return (sum);
}
